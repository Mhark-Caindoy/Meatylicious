			<h3>About Meatylicious</h3>
			<p>
				Meatylicious is located at Bucandala 1, Imus, Cavite.The Meat Shop's entire beef selection is certified all-natural, hormone-free, antibiotic-free, grass-fed, and grain-finished Wagyu Beef from Rosewood Ranches. Other offerings showcase hormone and antibiotic free pork, all-natural chicken, and a selection of smoked and cured meats and cheeses. Meatylicious is a traditional meatshop, that serves fresh meats that came from private and trusted poultry farmers. Meatylicious is focused on trading, processing and retail and wholesale of local and imported pork, beef, chicken and fish.
